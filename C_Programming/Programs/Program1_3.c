#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name :     Display
//  Description :        Print 5 to 1 decreasing order number
//  Input :             Integer
//  Output :            Integer
//  Author :            Abhishek Uttam Bankar
//  Date :              21/10/2022
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////
void Display()
{
    int i = 0;
    i = 5;
    while( i >= 1)
    {
        printf("%d\t",i);
        i--;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Entry point of the application
//////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}
