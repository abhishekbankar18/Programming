#include<stdio.h>

typedef int bool;
#define true 1
#define false 0
//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name :     Display
//  Description :       Accept one number And check whether it is divisible by 5 or not
//  Input :             Integer
//  Output :            boolean
//  Author :            Abhishek Uttam Bankar
//  Date :              21/10/2022
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////

bool Check(int iNo)
{
    if((iNo % 5) == 0)
    {
        return true;
    }
    else
    {
        return false;
    }
}


//////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Entry point of the application
//////////////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    bool bRet = false;

    printf("Enter number\n");
    scanf("%d",&iValue);

    bRet = Check(iValue);

    if(bRet == true)
    {
        printf("Divisible by 5");
    }
    else
    {
        printf("Not Divisible by 5");
    }

    return 0;
}
